    public void tabla(java.awt.event.ActionEvent evt){
        Connection con = null;
        String columnas, sql;
        try {
            con = getConnection();
//            Class.forName("com.mysql.jdbc.Driver");
//            con = DriverManager.getConnection("jdbc:mysql://localhost;3306/partido","root"," ");
            Statement st = con.createStatement();
            sql="select * from partido";
            ResultSet rstb=st.executeQuery(sql);
            ResultSetMetaData rsmd=rstb.getMetaData();
            int col=rsmd.getColumnCount();
            DefaultTableModel modelo = new DefaultTableModel();
            
            for(int i=1; i<=col;i++){
                modelo.addColumn(rsmd.getColumnLabel(i));
            }
            
            while (rstb.next()){
                String filas[]=new String[col];
                for(int j=0; j<col;j++){
                    filas[j]=rstb.getString(j+1);
                }
                modelo.addRow(filas);
            }
            
            tabla.setModel(modelo);
//            rstb.close();
//            con.close();
        } catch (Exception e) {
        }
    }
        
